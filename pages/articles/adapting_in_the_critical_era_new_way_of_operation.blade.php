                      <p class="mb-4 webcontent">During the pandemic, many companies have to run in a completely new structure due to social distancing. Employees are working from home and management is remote. What focuses should companies have to
                        adapt to the new environment during this critical time? <br>
                        <br><br>
                        <h5>1. Get Familiar with the Application</h5>
                        <p class="webcontent">When working from home becomes a common practice during the pandemic, it is important to maintain communication with the employees. Companies should know what they might need working from home, e.g.
                          computer, better network bandwidth, access to the database, etc.. When employees are working as individuals at their own home, communication does not only provide what they need at work but also build the bonding between the
                          company and employees, which is particularly important during the such a critical time. </p>
                        <br><br>
                        <h5>2. Constant Review</h5>
                        <p class="webcontent">During the pandemic, things are changing at a fast pace. Unexpected conditions happen from time to time. Regulations and policies are imposed upon emergencies. It is important for companies to constantly
                          review their practices on a weekly or even daily basis. Work in the way fits the current situation the most and abandon those outdated. This helps companies to adapt to the constantly changing environment in this critical
                          time.
                        </p>
                        <br><br>
                        <h5>3. Flexibility</h5>
                        <p class="webcontent">Uncertainty lies everywhere during this period, it is important for companies to be flexible. Teams might have to take up the responsibilities of the other team or work in a new way. Both the management
                          and employees should be open-minded and be ready to learn new stuff. Limiting oneself to the original or existing methods could be fatal in this critical era.</p>
                        <br></br>

                        <p class="webcontent">It has been a tough time for business, but if companies could have these focuses, they could be surviving through this pandemic and even develop during which. After all, business is about adapting the
                          ever-changing world ahead of others and generating profits from it.
                        </p>

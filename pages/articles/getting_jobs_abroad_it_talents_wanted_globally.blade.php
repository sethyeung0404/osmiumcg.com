                        <h5><u>Taiwan</u></h5>
                        <p class="webcontent">There is Employment Gold Card for employment-seeking individuals whose qualifications are reviewed and designated by the central competent authorities as Foreign Special Professionals. Professionals in
                          the IT sector are included in the 7 categories of Foreign Special Professionals. The Employment Gold Card is a combination of a resident visa issued by the Ministry of Foreign Affairs, a work permit issued by the Ministry of
                          Labor and an Alien Resident Certificate ( ARC ) and a re-entry permit both issued by the NIA. </p>
                        <br><br>
                        <h5><u>Germany</u></h5>
                        <p class="webcontent">In European Countries, there are EU Blue Card for professional to work in the countries.
                          <br><br>For Germany, people
                          <ol>
                            <li>with a German or an accredited foreign or a university degree that is comparable to a German one AND</li>
                            <li>have a working contract in so-called shortage occupations ( IT-skilled worker Included ) with the amount of €39.624 ( 3302 Euros per month ).</li>
                          </ol>can apply for the German EU Blue Card which would be valid for 4 years or for the time of the working contract plus 3 months. Blue Card owner could apply for permanent residence after 33 months.
                        </p>

                    <p class="mb-4 webcontent">During the COVID-19 pandemic, working from home becomes common due to social distancing or lockdown. Some companies decide to shift to working from home all the time even the pandemic is eased. For some
                      employees, working from is an ideal working style : Free, flexible and gives a sense of control. They have been waiting for this moment for so long. But is working from home that ideal? What might be some of the difficulties
                      that one have to overcome working from home?
                      <br>
                    <h5>1. Hard to focus</h5>
                    <p class="webcontent">The biggest enemy of working from home would be distractions. Unlike in office, there are so many temptations at home : Television, Netflix, Games. When there are no monitoring at home, one could be hard to
                      focus with all these distractions. Even if one is persistent, there could be interruptions from family and friends or even pets. This might not be an ideal working environment. To work from home, employees would have to use
                      different methods to keep themselves focused and finish their tasks.</p>

                    <h5>2. Lack of Communication</h5>
                    <p class="webcontent">In the modern economy, people are working with specialization. Most of the jobs would need to work with others to complete tasks. During this process, communication is required. Only through communication
                      could employees be able to know the progress or needs of their teammates and work accordingly. Working from home would bring huge challenges to communication between colleagues. If the majority of a company are working from
                      home, they could not just reach their teammate by simply going to their seat and ask. They would need to use instant communication applications to message or have a call or even a video call. There would be no way to ensure one
                      to reach out to another person immediately. Communication would thus be postponed. Besides, there could be technical problems during communication through applications, e.g. being laggy, disconnection, delay of audio and video.
                      This could be a huge threat to the companies so as employees’ efficiency.</p>

                    <h5>3. Lack of Resources</h5>
                    <p class="webcontent">When one is working in the office, they could get the resources they need easily. But when one is working from home, they might not get these resources as easily. They might need certain documents stored in
                      the company. This would be especially true for those companies who have not digitalized all their documents. Besides, employees might not have enough apparatuses at home as the company provided in the office. e.g. Computer,
                      Monitors, sufficient bandwidth. This could also bring huge obstacles to working from home as these apparatuses might not be easily accessible to employees and could cost quite a lot of money.</p>

                    <p class="webcontent">Of course, the above-mentioned difficulties are not invincible but could be overcome with the effort of either the employees or the companies. When working from home is becoming a trend across the world, it
                      is a good idea to know more about it and see how this new working style could. possibly change the economy, or even, the world
                    </p>

                    <p class="mb-4 webcontent">During the COVID-19 Pandemic, many things have changed from on-site to online, recruitment is not excluded. To keep the social distance and prevent the spread of the virus, many companies have
                      maintained a
                      work-from-home policy. Thus, on-site interviews are impossible and are replaced by online video interviews.
                      <br><br>How should candidates adapt to such a change? And how should they prepare for it? Take a look at the Dos and Don’ts below!
                      <br>
                    <h4 class="webcontent">DOs</h4>
                    <hr>
                    <h5>1. Get Familiar with the Application</h5>
                    <p class="webcontent">When online interviews are getting more and more common, there is a wide of choices on the application for the interview e.g. Zoom, Voov, Google Meet, Teams, Skype, etc.. Different application provides
                      different functions to
                      their users and has different ways of joining a conference. It is important for candidates to how the application function before the interview. How could one join the conference? Does it require a password? Could the
                      candidate
                      join the conference easily by pressing a link or they have to find the ID of the arranged meeting? It is not rare that candidates not familiar with the application face difficulties joining the conference so these should all
                      be
                      studied way before the interview. </p>

                    <h5>2. Check if Everything Works Well</h5>
                    <p class="webcontent">When it may seem to candidates that there is much less thing to prepare for an online interview than an on-site one, there are actually more things a candidate should check before the interview.
                      Candidates
                      should make sure the
                      internet connection works well and check if the camera and microphone are working properly. When the interaction between interviewers and interviewees are solely dependent on the computer, it would be a huge disaster when
                      the
                      computer malfunction. One would never want to be muted or blinded during an interview. So, candidates should always check everything before the interview and even better to prepare an extra set of apparatus, e.g. earphone,
                      microphones, cameras, etc.. </p>

                    <h5>3. Find a Quiet Place with no Interruption</h5>
                    <p class="webcontent">Although Work From Home is common during this period, it might not be a wise choice to Interview From Home. Construction in the neighbourhood, pets, babies, parents, they could all be possible
                      interruptions
                      during the interview
                      and you would like to avoid that. It could be a good choice to have a seat in some coffee shops. Renting a conference room in some open space is also possible. Ultimately, the principle is to find a place and make sure you
                      can
                      perform well in the interview: If your home is fine, then stay there. </p>
                    <br><br>
                    <h4 class="webcontent">DON'Ts</h4>
                    <hr>
                    <h5>1. Interview in front of a Terrible Background</h5>
                    <p class="webcontent">No matter where you are having your interview, it is important to make sure the background is presentable. Pick a decent background and avoid showing anything you would not want the interviewer to see:
                      pedestrians passing by the coffee shop window, shirts hanging around, pets or kids wandering around, or simply, an untidied desk. If you indeed do not have much choice on the background, some applications, e.g. Zoom, provide
                      the
                      function of virtual background. Users could replace their actual background with a picture they choose. Pick a decent background that gives a professional image and impress your interviewer! </p>

                    <h5>2. Be Punctual</h5>
                    <p class="webcontent">When you would arrive early at the venue for on-site interviews, do not be just punctual for online interviews. Try to get in the virtual conference room five or even fifteen minutes ahead. Some
                      interviewers
                      could be waiting in it already. When candidates do not need time for transportation to the on-site location, interviewers would have a much higher expectation of candidates’ punctuality. So, get ready and prepare all the
                      materials you need at least 30 minutes before the interview. Set up the place you are having the interview 15 mins ahead of time with all the documents you prepared placed in the right position for you to reach. The earlier
                      got
                      ready, the calmer you would be, and thus, the better you would perform.</p>

                    <h5>3. Running too much Browser Tabs or Applications</h5>
                    <p class="webcontent">When candidates are using computers for their video conference interviews, they tend to ‘utilize’ the computer and list as much research results as possible. This could make their computers laggy and
                      affect
                      the quality of the interview at the end. In a video conference interview, the quality of the video or connection should be of the first priority.
                      So, before the interview starts, close all the unnecessary tabs in the browser and close the applications not needed. Keep the computer running at its best performance and keep the video conference smooth. A smooth video
                      conference might not boost your performance, but a lag or even disconnection surely would ruin it.
                    </p>

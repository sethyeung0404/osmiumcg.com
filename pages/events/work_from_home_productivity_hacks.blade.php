                      <p class="mb-4 webcontent">During the pandemic, working from home becomes common. When it sounds like a pleasure to work from home, it could be torture when one could not be productive at work yet not relaxing at private time.
                        Below are some tips for those who work from home to stay productive!<br>
                        <br><br>
                      <h5>1. Dress Up</h5>
                      <p class="webcontent">Although it is cosy to stay in home wears, dressing up could help one to get in the state of working. One of the biggest challenges of working from home is that one could not be focused on work. There are
                        so many temptations at home and one do not feel like working. Dressing up helps one to have the sense that they are actually working and separate them from their original daily routine at home. The formality of clothing
                        boosts one’s performance working from home. </p>
                      <br></br>
                      <h5>2. Separation of Working Space</h5>
                      <p class="webcontent">When some of the employees are working from home, they might work in a position where they usually spend their leisure time, e.g. bedroom, sofa, etc. Similar to the dressing matter, the usual leisure
                        routine would blur the boundaries between working and relaxing at home. This makes employees less productive. To boost their productivity, one should set up a specific working space at home with only the required documents,
                        work phone and beverage. By minimizing the stuff in the workspace, one could be more focused on work.</p>
                      <br></br>
                      <h5>3. Time Management</h5>
                      <p class="webcontent">It is important to keep one’s time organized when working from home. As no one is monitoring their work, the productivity could be very low and they could find it lost. To boost productivity, employees
                        should have their time well planned. Making use of methods like Pomodoro Technique could help employees to make better use of their time and boost their productivity. By dividing time into sections, one would have clearer
                        goals and be more motivated. </p>
                      <br></br>

                      <p class="webcontent">Working from home is supposed to be a temporary measure for companies to run in the time of social distancing. Yet, some companies are taking it as a new way to work. It might be time for us to prepare
                        and get used to this new way of working.</p>
